class Calc:

    # 加法
    def addition(self, x, y):
        z = int(x) + int(y)
        return z

    # 减法
    def substarction(self, x, y):
        z = int(x) - int(y)
        return z

    # 乘法
    def multiplication(self, x, y):
        z = int(x) * int(y)
        return z

    # 除法
    def division(self, x, y):
        z = int(x) / int(y)
        return z
